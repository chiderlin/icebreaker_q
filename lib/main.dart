  String currentQuestion = '';
  final Random _random = Random();
  bool isFun = true;
  String currentType = '';

  void _generateRandomQuestion(){
    // final isFun = _random.nextBool();
    final questionSet = isFun? funQuestions : deepQuestions;
    setState((){
      currentType = isFun ? 'Fun Topic' : 'Deep Topic';
      currentQuestion = questionSet[_random.nextInt(questionSet.length)];
    });
  }

  @override
  void initState(){
    super.initState();
     isFun = _random.nextBool(); // after init, then can be invoked
    _generateRandomQuestion();
  }

  @override
  Widget build(BuildContext context){
    return MaterialApp(
      home: Scaffold(
        appBar: AppBar(
          title: Text('Chat Card Generator'),
          actions:[
            IconButton(
              icon: Icon(Icons.coffee),
              tooltip: 'Switch Topic',
              onPressed: (){
                setState((){
                  isFun = !isFun;
                });
                _generateRandomQuestion();
              }
            )
          ]
        ),
        body: Center(
          child: Padding(
            padding: const EdgeInsets.all(24),
            child: Column(
              mainAxisAlignment: MainAxisAlignment.center,
              children: [
